class Booking{
    constructor(startDate,endDate){
        this.startDate = startDate;
        this.endDate = endDate;
        this.authorized = false;
        this.authorizer = null;
        this.authorizedDate = null;

    }

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////FUNCTION TO RETURN NUMBER OF DAYS BOOKED FOR THIS BOOKING////////////////////////////////////////
    numberOfDays(){
        return ((this.endDate - this.startDate)/(1000*60*60*24));//converting to days from milliseconds
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////////////////////FUNCTION TO ALLOW MANAGER TO AUTHORISE A BOOKING////////////////////////////////////////////
    authorize(manager,date){
        if(date == undefined){
            this.authorized = true;
            this.authorizer = manager;
            this.authorizedDate = null;
            // this.authorizedDate = new Date();
        }else{
        this.authorized = true;
        this.authorizer = manager;
        this.authorizedDate = date;
        }
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////FUNCTION THAT RETURNS TRUE OR FALSE ON WHETHER BOOKING HAS BEEN AUTHORIZED///////////////////////////////
    isAuthorized(){
        return this.authorized;
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    /////////////////////////FUNCTION THAT RETURNS THE MANAGER WHO AUTHORIZED THE BOOKING//////////////////////////////////////////////
    authorizedBy(){
        return this.authorizer;
    }

    ///////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //////////////////////////////////FUNCTION THAT RETURNS THE DATE THE BOOKING WAS AUTHORIZED////////////////////////////////////////
    authorizedOn(){
        return this.authorizedDate;
    }

}













module.exports = Booking